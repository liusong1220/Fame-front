<template>
  <div>
    <div class="category-list">
      <a v-for="category in categories" class="category text-bold"
         @click='goAnchor("#category-"+category.name)'>
        {{category.name}}({{category.count}})
      </a>
    </div>
    <div class="category-content">
      <div class="divider"></div>
      <div v-for="category in categories">
        <div class="category-title" :id="'category-'+category.name">{{category.name}}</div>
        <ul class="category-ul">
          <li v-for="article in category.articles" class="article-title">
            <nuxt-link :to="{ path: '/article/'+article.id }" class="text-primary">{{article.title}}</nuxt-link>
          </li>
        </ul>
      </div>
    </div>
  </div>
</template>

<script type="text/ecmascript-6">
  import api from '~/plugins/api'

  export default {
    asyncData () {
      return api.getCategories().then((res) => {
        return {categories: res.data}
      })
    },
    data: function () {
      return {
        categories: []
      }
    },
    methods: {
      goAnchor (selector) {
        let anchor = this.$el.querySelector(selector)
        let position = anchor.offsetTop
        this.jump(position)
      },
      jump (total) {
        let distance = document.documentElement.scrollTop || document.body.scrollTop
        let step = total / 100
        if (total > distance) {
          smoothDown()
        } else {
          let newTotal = distance - total
          step = newTotal / 100
          smoothUp()
        }

        function smoothDown () {
          if (distance < total) {
            distance += step
            document.body.scrollTop = distance
            document.documentElement.scrollTop = distance
            setTimeout(smoothDown, 1)
          } else {
            document.body.scrollTop = total
            document.documentElement.scrollTop = total
          }
        }

        function smoothUp () {
          if (distance > total) {
            distance -= step
            document.body.scrollTop = distance
            document.documentElement.scrollTop = distance
            setTimeout(smoothUp, 1)
          } else {
            document.body.scrollTop = total
            document.documentElement.scrollTop = total
          }
        }
      }
    }
  }
</script>

